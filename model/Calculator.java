package model;

/**
 * Taschenrechner
 *
 * @author Daniel Appenmaier
 * @version 1.0
 *
 */
public class Calculator {

  /**
   * Addiert zwei Zahlen
   *
   * @param x Zahl 1
   * @param y Zahl 2
   * @return Summe von Zahl 1 und Zahl 2
   */
  public static int add(int x, int y) {
    return x + y;
  }

  /**
   * Subtrahiert zwei Zahlen
   *
   * @param x Zahl 1
   * @param y Zahl 2
   * @return Differenz von Zahl 1 und Zahl 2
   */
  public static int subtract(int x, int y) {
    return x - y;
  }

}
